# Load the dataset
athletes_count_by_country = pd.read_csv('data/athletes_count_by_country.csv')
medal_counts = pd.read_csv('data/summerOly_medal_counts.csv')
athletes_count_by_country = athletes_count_by_country.set_index('Year')
# columns are each NOC, rows are each year
# map NOC to country
athletes_count_by_country.columns = athletes_count_by_country.columns.map(country_translation)

# Logistic Regression
# 输入变量：历史运动员数量，输出变量：2024是否获奖
X = athletes_count_by_country.drop(2024)
# split X by country
X = X.T
# save NOC and Total
y = medal_counts[medal_counts['Year'] == 2024][['NOC', 'Total']]
X = X.merge(y, left_index=True, right_on='NOC', how='left')
# reset index
X = X.set_index('NOC')
# fill NaN
X = X.fillna(0)
# split X and y
y = X[['Total']]
X = X.drop(columns=['Total'])
# y to binary
y['WonMedal'] = (y['Total'] > 0).astype(int)  # 1 表示获奖，0 表示未获奖
y = y[['WonMedal']]
print(X)
print(y)

# regression
X_train, X_test, y_train, y_test = train_test_split(X, y, test_size=0.2)
model = RandomForestClassifier()
model.fit(X_train, y_train)
y_pred = model.predict(X_test)
print(accuracy_score(y_test, y_pred))
print(classification_report(y_test, y_pred))
